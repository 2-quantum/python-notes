import math

pi = 3.14
x = 1
y = 2
z = 3
a = 0
b = 100
print(round(pi))  # round off the decimal
print(math.ceil(pi))  # round off to the value which is greater than it
print(math.floor(pi))  # round off to the value which is smaller than it
print(abs(pi))  # same as mod in math
print(pow(pi, 2))  # pi^2
print(math.sqrt(pi))  # square root
print(min(pi, x, y, z, a, b))  # minimum
print(max(pi, x, y, z, a, b))  # maximum
